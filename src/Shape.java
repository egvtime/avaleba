public class Shape {
    int Area;
    int Perimiter;

    public Shape(int area, int perimiter) {
        Area = area;
        Perimiter = perimiter;
    }

    public int getArea() {
        return Area;
    }

    public void setArea(int area) {
        Area = area;
    }

    public int getPerimiter() {
        return Perimiter;
    }

    public void setPerimiter(int perimiter) {
        Perimiter = perimiter;
    }
}
