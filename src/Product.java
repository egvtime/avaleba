public class Product {
    int Date;
    int Price;

    public Product(int date, int price) {
        Date = date;
        Price = price;
    }

    public int getDate() {
        return Date;
    }

    public void setDate(int date) {
        Date = date;
    }

    public int getPrice() {
        return Price;
    }

    public void setPrice(int price) {
        Price = price;
    }

}
